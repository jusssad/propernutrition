class Ingredient {
    constructor(name, quantity) {
        this.name = name;
        this.quantity = quantity;
    }
}

// Класс для блюда
class Meal {
    constructor(name, type, calories, proteins, fats, carbs, ingredients, recipe) {
        this.name = name;
        this.type = type;  // Например, "Завтрак", "Обед", "Ужин"
        this.calories = calories;
        this.proteins = proteins;
        this.fats = fats;
        this.carbs = carbs;
        this.ingredients = ingredients;  // Массив ингредиентов
        this.recipe = recipe;  // Способ приготовления
    }
}

